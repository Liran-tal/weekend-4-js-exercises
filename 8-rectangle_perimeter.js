function getRectanglePerimeter(width, heigth) {
	return 2 * (width + heigth);
}

console.log("getRectanglePerimeter(6, 7): ", getRectanglePerimeter(6, 7));
console.log("getRectanglePerimeter(20, 10): ", getRectanglePerimeter(20, 10));
console.log("getRectanglePerimeter(2, 9): ", getRectanglePerimeter(2, 9));